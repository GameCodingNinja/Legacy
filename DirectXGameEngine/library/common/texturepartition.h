// The partitions are used to determine the positions of the textures
class CTexturePartition
{
public:
        
    // Constructors
    CTexturePartition():vacant(true){}

    // The position of the partition
    CPointInt pos;

    // Size of the partition
    CSize<int> size;

    // Whether or not the partition is full
    bool vacant;

};

